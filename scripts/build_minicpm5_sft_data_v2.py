def fam_warehouse(rng: random.Random) -> list[dict]:
    start = rng.choice(range(1200, 8001, 200))
    pct = rng.choice([15, 20, 25, 30, 35, 40, 45, 55, 60])
    restock = rng.choice(range(300, 1501, 50))
    after1 = start * (100 - pct) // 100
    assert start * (100 - pct) % 100 == 0
    sold3 = rng.choice(range(200, min(after1 + restock - 100, 2500), 20))
    remaining = after1 + restock - sold3
    site = rng.choice(["warehouse", "distribution centre", "depot", "fulfilment centre"])
    prompt = (f"A {site} starts with {start:,} units. In Q1 it sells {pct}% of stock. In Q2 it restocks "
              f"{restock} units. In Q3 it sells {sold3} units. How many units remain at the end of Q3?")
    code = (f"start = {start}\npct = {pct}\nafter_q1 = start * (100 - pct) // 100\n"
            f"after_q2 = after_q1 + {restock}\nremaining = after_q2 - {sold3}\nprint(remaining)")
    return rows(prompt, code, f"{remaining} units")


def fam_recipe(rng: random.Random) -> list[dict]:
    while True:
        num, den = rng.choice([(1, 2), (1, 4), (3, 4), (2, 3), (3, 5), (5, 8), (2, 5), (7, 8)])
        base = rng.choice([6, 8, 10, 12, 15, 16, 20, 24])
        target = rng.choice([9, 14, 18, 21, 25, 26, 28, 30, 32, 36, 40])
        needed = Fraction(num, den) * Fraction(target, base)
        if needed == 1 or needed.denominator == 1 or 10_000 % needed.denominator:
            continue
        price = rng.choice([1.2, 1.5, 1.6, 1.8, 2.0, 2.4, 2.5, 3.2, 4.0])
        cost = float(needed) * price
        if abs(cost - round(cost, 2)) > 1e-9:
            continue
        break
    ing, item = rng.choice([("sugar", "cookies"), ("flour", "muffins"), ("butter", "scones"),
                            ("cocoa", "brownies"), ("oats", "flapjacks")])
    prompt = (f"A recipe requires {num}/{den} cup of {ing} for {base} {item}. How much {ing} is needed "
              f"for {target} {item}? If {ing} costs ${price:.2f} per cup, what is the total cost of "
              f"{ing} for {target} {item}?")
    code = (f"from fractions import Fraction\nneeded = Fraction({num}, {den}) * Fraction({target}, {base})\n"
            f"cost = float(needed) * {price}\nprint(f'{{float(needed):g}} cups')\nprint(f'${{cost:.2f}}')")
    return rows(prompt, code, f"{float(needed):g} cups of {ing}; total cost ${cost:.2f}.")


def fam_trains(rng: random.Random) -> list[dict]:
    while True:
        v_a = rng.choice(range(60, 121, 5))
        v_b = rng.choice(range(60, 121, 5))
        gap_min = rng.choice([60, 75, 90, 105, 120])
        dep_a_h, dep_a_m = rng.choice([(6, 0), (6, 45), (7, 0), (7, 30), (8, 0), (8, 15), (9, 0)])
        dep_b_total = dep_a_h * 60 + dep_a_m + gap_min
        head_h = gap_min / 60
        dist = rng.choice(range(300, 651, 10))
        covered = v_a * head_h
        remaining = dist - covered
        if remaining <= 0:
            continue
        secs = remaining * 3600 / (v_a + v_b)
        if abs(secs - round(secs)) > 1e-9:
            continue
        secs = round(secs)
        from_a = v_a * (head_h + secs / 3600)
        if abs(from_a - round(from_a, 2)) > 1e-9:
            continue
        break
    meet_total = dep_b_total * 60 + secs
    mh, rem = divmod(meet_total, 3600)
    mm, ms = divmod(rem, 60)
    meet = f"{mh:02d}:{mm:02d}:{ms:02d}" if ms else f"{mh:02d}:{mm:02d}"
    dep_a = f"{dep_a_h:02d}:{dep_a_m:02d}"
    dep_b = f"{dep_b_total // 60:02d}:{dep_b_total % 60:02d}"
    prompt = (f"A train leaves City A at {dep_a} travelling toward City B at {v_a} km/h. A second train "
              f"leaves City B at {dep_b} travelling toward City A at {v_b} km/h. The distance between "
              f"the cities is {dist} km. At what time do the trains meet, and how far from City A is "
              f"the meeting point?")
    code = (f"from datetime import datetime, timedelta\ndistance = {dist}\nv_a = {v_a}\nv_b = {v_b}\n"
            f"start_a = datetime(2000, 1, 1, {dep_a_h}, {dep_a_m})\n"
            f"start_b = datetime(2000, 1, 1, {dep_b_total // 60}, {dep_b_total % 60})\n"
            f"head_h = (start_b - start_a).total_seconds() / 3600\n"
            f"remaining = distance - v_a * head_h\nafter_b_h = remaining / (v_a + v_b)\n"
            f"meet = start_b + timedelta(hours=after_b_h)\nfrom_a = v_a * (head_h + after_b_h)\n"
            f"assert abs(from_a / v_a - (head_h + after_b_h)) < 1e-9\n"
            f"fmt = '%H:%M:%S' if meet.second else '%H:%M'\n"
            f"print(meet.strftime(fmt))\nprint(f'{{from_a:g}} km from City A')")
    return rows(prompt, code, f"They meet at {meet}, {from_a:g} km from City A.")


def fam_revenue(rng: random.Random) -> list[dict]:
    half = rng.choice([["Jan", "Feb", "Mar", "Apr", "May", "Jun"], ["Jul", "Aug", "Sep", "Oct", "Nov", "Dec"]])
    nxt = {"Jun": "Jul", "Dec": "Jan"}[half[-1]]
    base = rng.choice(range(80, 260, 10)) * 1000
    figures = [base]
    for _ in range(5):
        figures.append(int(figures[-1] * (1 + rng.choice([-8, -6, -4, -3, -2, 2, 3, 4, 6, 8, 10, 12]) / 100)))
    avg = sum(figures) / len(figures)
    rates, declines = [], []
    for i in range(1, 6):
        r = (figures[i] - figures[i - 1]) / figures[i - 1] * 100
        rates.append((half[i], r))
        if figures[i] < figures[i - 1]:
            declines.append(half[i])
    proj = figures[-1] * (1 + sum(r for _, r in rates[-3:]) / 3 / 100)
    figs_txt = " | ".join(f"{m} ${v:,}" for m, v in zip(half, figures))
    growth_txt = ", ".join(f"{m} {r:.1f}%" for m, r in rates)
    prompt = (f"Monthly revenue figures: {figs_txt}.\n\nCalculate all of the following:\n"
              f"1. Average monthly revenue across all six months.\n"
              f"2. Month-over-month growth rate for each month from {half[1]} onwards (as a percentage, "
              f"rounded to one decimal place).\n3. Which months saw a revenue decline?\n"
              f"4. Projected {nxt} revenue using the average of the last three months' growth rates.")
    code = (f"months = {half!r}\nrev = {figures!r}\navg = sum(rev) / len(rev)\n"
            f"rates = []\ndeclines = []\n"
            f"for i in range(1, len(rev)):\n"
            f"    r = (rev[i] - rev[i - 1]) / rev[i - 1] * 100\n"
            f"    rates.append((months[i], r))\n"
            f"    if rev[i] < rev[i - 1]:\n        declines.append(months[i])\n"
            f"last3 = [r for _, r in rates[-3:]]\nassert len(last3) == 3\n"
            f"proj = rev[-1] * (1 + sum(last3) / 3 / 100)\n"
            f"print(f'average=${{avg:,.2f}}')\n"
            f"print('growth=' + ', '.join(f'{{m}} {{r:.1f}}%' for m, r in rates))\n"
            f"print('declines=' + (', '.join(declines) or 'none'))\n"
            f"print(f'proj=${{proj:,.2f}}')")
    answer = (f"1. Average monthly revenue: ${avg:,.2f}. 2. Growth: {growth_txt}. "
              f"3. Declines: {', '.join(declines) if declines else 'none'}. "
              f"4. Projected {nxt} revenue: ${proj:,.2f}.")
    return rows(prompt, code, answer)


def fam_tank(rng: random.Random) -> list[dict]:
    start = rng.choice(range(300, 901, 20))
    d1, t1 = rng.choice(range(4, 13)), rng.choice(range(8, 21))
    f2, t2 = rng.choice(range(8, 19)), rng.choice(range(10, 25))
    d3, t3 = rng.choice(range(3, 10)), rng.choice(range(10, 25))
    final = start - d1 * t1 + f2 * t2 - d3 * t3
    if final <= 0:
        return fam_tank(rng)
    vessel = rng.choice(["tank", "reservoir", "cistern"])
    prompt = (f"A {vessel} starts with {start} litres. It drains at {d1} litres per minute for {t1} "
              f"minutes, is then refilled at {f2} litres per minute for {t2} minutes, then drains at "
              f"{d3} litres per minute for {t3} minutes. How many litres are in the {vessel} now?")
    code = (f"start = {start}\nafter_drain1 = start - {d1} * {t1}\n"
            f"after_fill = after_drain1 + {f2} * {t2}\nfinal = after_fill - {d3} * {t3}\nprint(final)")
    return rows(prompt, code, f"{final} litres")


def fam_pipes(rng: random.Random) -> list[dict]:
    a, b = rng.sample([3, 4, 5, 6, 8, 10, 12], 2)
    hours = 1 / (1 / a + 1 / b)
    prompt = (f"Pump A can fill a tank in {a} hours. Pump B can fill the same tank in {b} hours. "
              f"If both pumps run together, how many hours does filling take? Answer as a decimal "
              f"rounded to 2 decimal places.")
    code = (f"a = {a}\nb = {b}\nhours = 1 / (1 / a + 1 / b)\nprint(f'{{hours:.2f}}')")
    return rows(prompt, code, f"{hours:.2f} hours")


def fam_percent_chain(rng: random.Random) -> list[dict]:
    start = rng.choice(range(100, 901, 50))
    p1 = rng.choice([10, 15, 20, 25, 30])
    p2 = rng.choice([10, 15, 20, 25, 30])
    p3 = rng.choice([5, 8, 10, 12])
    final = start * (1 + p1 / 100) * (1 - p2 / 100) * (1 + p3 / 100)
    if abs(final - round(final, 2)) > 1e-9:
        return fam_percent_chain(rng)
    prompt = (f"A price is increased by {p1}%, then decreased by {p2}%, then increased by {p3}%. "
              f"If the original price was ${start}, what is the final price?")
    code = (f"start = {start}\nfinal = start * (1 + {p1}/100) * (1 - {p2}/100) * (1 + {p3}/100)\n"
            f"print(f'${{final:.2f}}')")
    return rows(prompt, code, f"${final:.2f}")


