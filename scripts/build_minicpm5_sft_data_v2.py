FURRY = {"cat", "dog", "hamster", "rabbit"}


def fam_assignment(rng: random.Random) -> list[dict]:
    n = rng.choice([3, 4])
    people = rng.sample(FIRST_NAMES, n)
    domain_name, items = rng.choice([
        ("beverage", ["coffee", "tea", "water", "juice", "cocoa"]),
        ("pet", ["cat", "dog", "parrot", "hamster", "goldfish", "rabbit", "canary"]),
        ("instrument", ["guitar", "piano", "violin", "drums", "flute"]),
        ("sport", ["tennis", "rowing", "cycling", "boxing", "karate"]),
    ])
    items = rng.sample(items, n)
    solution = dict(zip(people, rng.sample(items, n)))

    clue_texts: list[str] = []
    preds = []
    fixed = rng.choice(people)
    clue_texts.append(f"{fixed} {'drinks' if domain_name == 'beverage' else 'has'} {solution[fixed]}.")
    preds.append(lambda a, p=fixed, it=solution[fixed]: a[p] == it)
    for p in people:
        if p == fixed:
            continue
        # semantic exclusion clue: 'allergic to fur' bans every furry item and
        # the CODE must encode that world knowledge as an explicit set.
        furry_here = [i for i in items if i in FURRY]
        if (domain_name == "pet" and solution[p] not in FURRY and furry_here
                and rng.random() < 0.5):
            clue_texts.append(f"{p} is allergic to fur.")
            preds.append(lambda a, p=p, banned=frozenset(furry_here): a[p] not in banned)
            continue
        wrongs = [i for i in items if i != solution[p]]
        k = rng.choice([1, 2]) if n == 4 else 1
        picked = rng.sample(wrongs, min(k, len(wrongs)))
        if len(picked) == 2:
            clue_texts.append(f"{p} has neither the {picked[0]} nor the {picked[1]}.")
        else:
            clue_texts.append(f"{p} does not have the {picked[0]}.")
        preds.append(lambda a, p=p, banned=frozenset(picked): a[p] not in banned)

    sols = [dict(zip(people, perm)) for perm in itertools.permutations(items)
            if all(pr(dict(zip(people, perm))) for pr in preds)]
    if len(sols) != 1 or sols[0] != solution:
        return fam_assignment(rng)  # resample until unique

    people_txt = ", ".join(people[:-1]) + f", and {people[-1]}"
    items_txt = ", ".join(items[:-1]) + f", or {items[-1]}"
    clues = "\n".join(f"{i + 1}. {c}" for i, c in enumerate(clue_texts))
    prompt = (f"{'Four' if n == 4 else 'Three'} friends — {people_txt} — each have a different "
              f"{domain_name}: {items_txt}. Use the following clues to determine who has what:\n{clues}\n"
              f"State each person's {domain_name}.")
    # rebuild the clue predicates as literal python conditions for the code string
    code_pre = ""
    code_conds = [f"d[{fixed!r}] == {solution[fixed]!r}"]
    for text in clue_texts[1:]:
        who = text.split()[0]
        if "allergic to fur" in text:
            code_pre = f"furred = {sorted(i for i in items if i in FURRY)!r}\n"
            code_conds.append(f"d[{who!r}] not in furred")
            continue
        banned = [i for i in items if f"the {i}" in text]
        if len(banned) == 2:
            code_conds.append(f"d[{who!r}] not in ({banned[0]!r}, {banned[1]!r})")
        else:
            code_conds.append(f"d[{who!r}] != {banned[0]!r}")
    cond_expr = " and ".join(code_conds)
    code = (f"import itertools\npeople = {people!r}\nitems = {items!r}\n{code_pre}sols = []\n"
            f"for perm in itertools.permutations(items):\n"
            f"    d = dict(zip(people, perm))\n"
            f"    if {cond_expr}:\n        sols.append(d)\n"
            f"assert len(sols) == 1\nd = sols[0]\n"
            f"print('; '.join(f'{{p}}: {{d[p]}}' for p in people))")
    answer = "; ".join(f"{p}: {solution[p]}" for p in people) + "."
    return rows(prompt, code, answer)


def fam_seating(rng: random.Random) -> list[dict]:
    people = rng.sample(FIRST_NAMES, 5)
    order = people[:]
    rng.shuffle(order)
    first, last = order[0], order[4]
    # adjacent pair clue from the true order
    idx = rng.choice([1, 2])
    left_p, right_p = order[idx], order[idx + 1]
    # non-adjacent clue: pick someone genuinely not next to `last`
    non_adj_candidates = [p for p in order[1:4] if abs(order.index(p) - 4) > 1]
    if not non_adj_candidates:
        return fam_seating(rng)
    loner = rng.choice(non_adj_candidates)

    sols = []
    for perm in itertools.permutations(people):
        if perm[0] != first or perm[4] != last:
            continue
        li, ri = perm.index(left_p), perm.index(right_p)
        if ri - li != 1:
            continue
        if abs(perm.index(loner) - perm.index(last)) == 1:
            continue
        sols.append(perm)
    if len(sols) != 1 or list(sols[0]) != order:
        return fam_seating(rng)

    people_txt = ", ".join(people[:-1]) + f", and {people[-1]}"
    prompt = (f"Five colleagues — {people_txt} — sit in five seats numbered 1 to 5 in a row (seat 1 is "
              f"the far left). {first} sits in seat 1. {last} sits in seat 5. {left_p} sits immediately "
              f"to the left of {right_p}. {loner} does not sit next to {last}. "
              f"What is the seating order from seat 1 to seat 5?")
    code = (f"import itertools\npeople = {people!r}\nsols = []\n"
            f"for perm in itertools.permutations(people):\n"
            f"    if perm[0] != {first!r} or perm[4] != {last!r}:\n        continue\n"
            f"    if perm.index({right_p!r}) - perm.index({left_p!r}) != 1:\n        continue\n"
            f"    if abs(perm.index({loner!r}) - perm.index({last!r})) == 1:\n        continue\n"
            f"    sols.append(perm)\n"
            f"assert len(sols) == 1\nprint(', '.join(sols[0]))")
    return rows(prompt, code, ", ".join(order) + ".")


def fam_eval_code(rng: random.Random) -> list[dict]:
    """'What does this evaluate to' semantics gotchas: the tool call RUNS the
    snippet and the final answer states the real values plus the mechanism."""
    kind = rng.choice(["late_binding", "mutable_default", "generator", "aliasing", "float_repr"])
    if kind == "late_binding":
        lo = rng.choice([1, 2, 3])
        hi = lo + 3
        arg = rng.choice([10, 20, 100])
        op, opname = rng.choice([("+", "x + i"), ("*", "x * i")])
        snippet = (f"def make_fns():\n    return [lambda x: x {op} i for i in range({lo}, {hi})]\n\n"
                   f"fns = make_fns()\nresults = [f({arg}) for f in fns]")
        mechanism = (f"the lambdas close over the loop variable i by reference (late binding), and i is "
                     f"{hi - 1} for all of them once the comprehension finishes")
        show = ["results"]
    elif kind == "mutable_default":
        a, b = rng.sample(range(1, 9), 2)
        snippet = (f"def add_item(item, box=[]):\n    box.append(item)\n    return box\n\n"
                   f"first = add_item({a})\nsecond = add_item({b})")
        mechanism = ("default argument values are evaluated once at definition time, so every call "
                     "without box shares the same mutable list")
        show = ["first", "second"]
    elif kind == "generator":
        n = rng.choice([6, 8, 10])
        nums = list(range(1, n + 1))
        snippet = (f"nums = {nums}\nevens = (x for x in nums if x % 2 == 0)\n"
                   f"total1 = sum(evens)\ntotal2 = sum(evens)")
        mechanism = "the generator is exhausted by the first sum(), so the second sum() sees nothing"
        show = ["total1", "total2"]
    elif kind == "aliasing":
        base = rng.sample(range(1, 20), 3)
        extra = rng.choice(range(20, 40))
        snippet = (f"a = {base}\nb = a\nb.append({extra})\nlength_a = len(a)\nsame = a is b")
        mechanism = "b = a binds a second name to the SAME list object, so mutating b mutates a"
        show = ["length_a", "same"]
    else:  # float_repr
        x, y = rng.choice([(0.1, 0.2), (0.1, 0.7), (0.2, 0.4)])
        target = round(x + y, 1)
        snippet = (f"total = {x} + {y}\nexact = total == {target}")
        mechanism = (f"binary floating point cannot represent {x} and {y} exactly, so their sum is not "
                     f"exactly {target}")
        show = ["exact", "total"]

    ns: dict[str, object] = {}
    exec(snippet, ns, ns)  # noqa: S102
    values = {k: ns[k] for k in show}
    names = " and ".join(f"`{k}`" for k in show)
    prompt = (f"What {'does' if len(show) == 1 else 'do'} {names} evaluate to, and why, given this "
              f"code?\n\n{snippet}")
    code = snippet + "\n" + "\n".join(f"print({k!r}, '=', repr({k}))" for k in show)
    answer = "; ".join(f"{k} = {values[k]!r}" for k in show) + f" - {mechanism}."
    return rows(prompt, code, answer)


def fam_weights(rng: random.Random) -> list[dict]:
    """Boxes with pairwise sum/diff constraints (the author's adversarial shape)."""
    boxes = ["P", "Q", "R", "S", "T"]
    weights = [2, 4, 6, 8, 10]
    solution = dict(zip(boxes, rng.sample(weights, 5)))
    pair = rng.sample(boxes, 2)
    pair_sum = solution[pair[0]] + solution[pair[1]]
    heavier = rng.sample([b for b in boxes if b not in pair], 2)
    if solution[heavier[0]] < solution[heavier[1]]:
        heavier.reverse()
    top = max(boxes, key=lambda b: solution[b])
    diff_pair = rng.sample(boxes, 2)
    diff = abs(solution[diff_pair[0]] - solution[diff_pair[1]])

    conds = [
        (f"{pair[0]} and {pair[1]} together weigh {pair_sum} kg.",
         f"w['{pair[0]}'] + w['{pair[1]}'] == {pair_sum}"),
        (f"{heavier[0]} is heavier than {heavier[1]}.",
         f"w['{heavier[0]}'] > w['{heavier[1]}']"),
        (f"{top} is the heaviest box.",
         f"w['{top}'] == max(w.values())"),
        (f"{diff_pair[0]} and {diff_pair[1]} differ by exactly {diff} kg.",
         f"abs(w['{diff_pair[0]}'] - w['{diff_pair[1]}']) == {diff}"),
    ]
    sols = []
    for perm in itertools.permutations(weights):
        w = dict(zip(boxes, perm))
        if all(eval(c, {"w": w}) for _, c in conds):  # noqa: S307 - our own literals
            sols.append(w)
    if len(sols) != 1 or sols[0] != solution:
        return fam_weights(rng)
    clue_txt = " ".join(t for t, _ in conds)
    prompt = (f"Five boxes — P, Q, R, S, and T — contain weights of 2, 4, 6, 8, and 10 kg, one weight "
              f"per box. {clue_txt} What is the weight of each box?")
    cond_expr = " and ".join(c for _, c in conds)
    code = (f"import itertools\nboxes = {boxes!r}\nsols = []\n"
            f"for perm in itertools.permutations([2, 4, 6, 8, 10]):\n"
            f"    w = dict(zip(boxes, perm))\n"
            f"    if {cond_expr}:\n        sols.append(w)\n"
            f"assert len(sols) == 1\nw = sols[0]\n"
            f"print('; '.join(f'{{b}}: {{w[b]}} kg' for b in boxes))")
    answer = "; ".join(f"{b}: {solution[b]} kg" for b in boxes) + "."
    return rows(prompt, code, answer)


FAMILIES = [
    (fam_warehouse, 40), (fam_recipe, 40), (fam_trains, 40), (fam_revenue, 40),
    (fam_tank, 30), (fam_pipes, 20), (fam_percent_chain, 30), (fam_production, 25),
    (fam_assignment, 50), (fam_seating, 35), (fam_eval_code, 40), (fam_weights, 25),
]


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--out", default="finetune/minicpm5/data/minicpm5_yassai_v2.jsonl")
    ap.add_argument("--seed", type=int, default=20260710)
    args = ap.parse_args()
    rng = random.Random(args.seed)
    out: list[dict] = []
    seen_prompts: set[str] = set()
    for fam, count in FAMILIES:
        made = 0
        while made < count:
            pair = fam(rng)
            p = pair[0]["messages"][1]["content"]
            if p in seen_prompts:
                continue
            seen_prompts.add(p)
            out.extend(pair)
            made += 1
    # World-knowledge exclusion clues (allergic-to-fur) appear only when the
    # pet domain and solution alignment coincide, so they are rare in the mix -
    # and v2e3b still mis-encodes them at eval. Triplicate those cases so the
    # exclusion pattern carries real gradient weight.
    boosted: list[dict] = []
    for i in range(0, len(out), 2):
        pair = out[i:i + 2]
        boosted.extend(pair)
        if "allergic" in pair[0]["messages"][1]["content"]:
            boosted.extend(pair)
            boosted.extend(pair)
    out = boosted
    path = Path(args.out)
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as f:
        for r in out:
            f.write(compact_json(r) + "\n")
    print(f"wrote {len(out)} rows ({len(out) // 2} cases) to {path}")


if __name__ == "__main__":
    main()
