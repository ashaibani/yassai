#!/usr/bin/env python3
"""Build the v2 MiniCPM5 SFT dataset: parameterised, execution-verified maths +
logic tool-calling at scale.

v1 shipped ~17 hand-written cases (plus exact copies of the sample test tasks,
which the Participant Guide forbids training on and which cannot help on the
unseen prompt variants the leaderboard actually scores). v2 generates hundreds
of randomised instances across the benchmark's task families. Every case is
executed while building: the observation is real stdout, and the final answer
is INTERPOLATED from the executed namespace - never hand-typed - so the
dataset itself embodies the discipline the agent needs.

Two rows per case (same contract as v1 / eval_tool_behavior.py):
  1. user prompt                       -> assistant emits one <tool_call>
  2. prompt + run_python result + ask  -> assistant emits the final answer
"""

from __future__ import annotations

import argparse
import contextlib
import io
import itertools
import json
import random
from fractions import Fraction
from pathlib import Path

# The tool turn is the JSON tool call MiniCPM5 was pretrained on. A fenced-code
# alternative was tried to dodge GGUF's </tool_call> mangling but REGRESSED the
# 1B LoRA (syntax errors, repeat-loop degeneration even at F16), so JSON stays;
# the mangled closing tag is handled by the runtime's balanced-brace parser.
SYSTEM = """You are yassai-local, a small local specialist for math and logic tasks.
Use run_python for arithmetic, percentages, schedules, projections, combinatorics, and constraint checks.
Never do these calculations in your head.
When a tool is needed, respond with exactly one tool call and no prose:
<tool_call>{"name":"run_python","arguments":{"code":"..."}}</tool_call>
The Python must compute from named variables and print concise final values.
After receiving a run_python result, return only the final answer requested by the user."""

FIRST_NAMES = ["Alice", "Ben", "Carla", "Dmitri", "Elena", "Farid", "Grace", "Hugo", "Iris", "Jonas",
               "Kira", "Leo", "Mona", "Nils", "Olga", "Pablo", "Quinn", "Rosa", "Sam", "Tessa",
               "Umar", "Vera", "Wes", "Xena", "Yusuf", "Zoe", "Priya", "Liam", "Emma", "Noah"]


def compact_json(obj: object) -> str:
    return json.dumps(obj, ensure_ascii=False, separators=(",", ":"))


def tool_call(code: str) -> str:
    return "<tool_call>" + compact_json({"name": "run_python", "arguments": {"code": code.strip()}}) + "</tool_call>"


def rows(prompt: str, code: str, answer: str) -> list[dict]:
    code = code.strip()
    buf = io.StringIO()
    ns: dict[str, object] = {}
    with contextlib.redirect_stdout(buf):
        exec(code, ns, ns)  # noqa: S102 - generated by us, verified here
    observation = buf.getvalue().strip()
    assert observation, f"code produced no output:\n{code}"
    after = f"{prompt.strip()}\n\nrun_python result:\n{observation}\n\nReturn the final answer only."
    return [
        {"messages": [{"role": "system", "content": SYSTEM},
                      {"role": "user", "content": prompt.strip()},
                      {"role": "assistant", "content": tool_call(code)}]},
        {"messages": [{"role": "system", "content": SYSTEM},
                      {"role": "user", "content": after},
                      {"role": "assistant", "content": answer.strip()}]},
    ]


